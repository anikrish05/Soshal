class ClubCardData {
  final List<String> admin;
  final String category;
  final String description;
  final String downloadURL;
  final List<String> events;
  final List<String> followers;
  final String name;
  final String type;
  final bool verified;

  ClubCardData({required this.admin, required this.category, required this.description,
    required this.downloadURL, required this.followers, required this.events, required this.name, required this.type, required this.verified});

}